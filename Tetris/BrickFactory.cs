        public Brick getBricks(int shape)
        {
            int[][][] bricks = new int[4][][];
            for(int i = 0; i < 4; i++)
            {
                bricks[i] = new int[4][];
                for(int j = 0; j < 4; j++)
                {
                    bricks[i][j] = new int[4];
                }
            }

            for(int i = 0; i < 4; i++)
            {
                for(int j = 0; j < 4; j++)
                {
                    for(int k = 0; k < 4; k++)
                    {
                        bricks[i][j][k] = allShapes[shape, i, j, k];
                    }
                }
            }
            Random r = new Random();
            Brick aBrick = new Brick(bricks, r.Next(0, 4));
            return aBrick;
        }

